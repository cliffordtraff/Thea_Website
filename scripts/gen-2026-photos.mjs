/*
 * gen-2026-photos.mjs
 * -------------------
 * One-shot generator for the 2026 photo refresh.
 *
 * Reads the delivered photo set ("Updated Website Photos 7.27.26"), copies each
 * image into /public/images/2026/<section>/ with normalized names, reads its
 * true pixel dimensions with macOS `sips`, and regenerates the content files so
 * every photo tab references ONLY the new imagery:
 *
 *   Inside          -> content/commissions.ts   (2. INSIDE)
 *   Outside         -> content/personal.ts      (3. OUTSIDE)
 *   Dance           -> content/dance.ts          (4. DANCE)   [new]
 *   Elevator Series -> content/elevator.ts      (5. ELEVATOR SERIES)
 *   Landing page    -> content/home.ts          (LP.jpg only)
 *
 * Run from the Next project root:  node scripts/gen-2026-photos.mjs
 */
import { execSync } from "node:child_process";
import {
  readdirSync,
  copyFileSync,
  mkdirSync,
  writeFileSync,
  rmSync,
  existsSync,
} from "node:fs";
import path from "node:path";

const PROJECT = process.cwd();
const SRC = path.resolve(PROJECT, "..", "Updated Website Photos 7.27.26");
const OUT_DIR = path.join(PROJECT, "public", "images", "2026");
const CONTENT = path.join(PROJECT, "content");

if (!existsSync(SRC)) {
  console.error(`Source folder not found: ${SRC}`);
  process.exit(1);
}

/** True pixel dimensions via macOS sips (no external deps). */
function dims(file) {
  const out = execSync(
    `sips -g pixelWidth -g pixelHeight ${JSON.stringify(file)}`,
    { encoding: "utf8" }
  );
  const w = Number(out.match(/pixelWidth:\s*(\d+)/)?.[1]);
  const h = Number(out.match(/pixelHeight:\s*(\d+)/)?.[1]);
  if (!w || !h) throw new Error(`Could not read dimensions for ${file}`);
  return { w, h };
}

const IMG_RE = /\.(jpe?g|png)$/i;
const numOf = (name) => {
  const m = name.match(/(\d+)/);
  return m ? Number(m[1]) : 0;
};

/**
 * Copy + measure one section's images.
 * Returns [{ src, width, height }] in numeric filename order.
 */
function processSection(srcSub, prefix) {
  const dir = path.join(SRC, srcSub);
  const files = readdirSync(dir)
    .filter((f) => IMG_RE.test(f))
    .sort((a, b) => numOf(a) - numOf(b));

  const destDir = path.join(OUT_DIR, prefix);
  rmSync(destDir, { recursive: true, force: true });
  mkdirSync(destDir, { recursive: true });

  return files.map((f, i) => {
    const n = String(i + 1).padStart(2, "0");
    const destName = `${prefix}-${n}.jpg`;
    const from = path.join(dir, f);
    const to = path.join(destDir, destName);
    copyFileSync(from, to);
    const { w, h } = dims(from);
    return { src: `/images/2026/${prefix}/${destName}`, width: w, height: h };
  });
}

const ALT = "Photograph by Thea Traff";

/** Serialize an image list into `blocks: [...]` entries. */
function blocksFor(images) {
  return images
    .map(
      (im) => `    {
      variant: "centered-portrait",
      image: {
        src: "${im.src}",
        width: ${im.width},
        height: ${im.height},
        alt: ${JSON.stringify(ALT)},
      },
    },`
    )
    .join("\n");
}

function projectFile({ exportName, title, slug, fileName, srcSub, prefix }) {
  const images = processSection(srcSub, prefix);
  const body = `import type { Project } from "./types";

/*
 * ${title} — real photographs.
 * GENERATED by scripts/gen-2026-photos.mjs from "Updated Website Photos 7.27.26".
 * ${images.length} photographs. Re-run the script to regenerate.
 */
export const ${exportName}: Project = {
  title: ${JSON.stringify(title)},
  slug: ${JSON.stringify(slug)},
  blocks: [
${blocksFor(images)}
  ],
};
`;
  writeFileSync(path.join(CONTENT, `${fileName}.ts`), body);
  return images.length;
}

// --- Sections ---------------------------------------------------------------
mkdirSync(OUT_DIR, { recursive: true });

const counts = {};
counts.inside = projectFile({
  exportName: "commissions",
  title: "Recent Commissions",
  slug: "commissions",
  fileName: "commissions",
  srcSub: "2. INSIDE",
  prefix: "inside",
});
counts.outside = projectFile({
  exportName: "personal",
  title: "Personal Work",
  slug: "personal",
  fileName: "personal",
  srcSub: "3. OUTSIDE",
  prefix: "outside",
});
counts.dance = projectFile({
  exportName: "dance",
  title: "Dance",
  slug: "dance",
  fileName: "dance",
  srcSub: "4. DANCE",
  prefix: "dance",
});
counts.elevator = projectFile({
  exportName: "elevator",
  title: "Elevator Series",
  slug: "elevator-series",
  fileName: "elevator",
  srcSub: "5. ELEVATOR SERIES",
  prefix: "elevator",
});

// --- Landing page (single LP image) -----------------------------------------
{
  const lpFrom = path.join(SRC, "LP.jpg");
  const lpTo = path.join(OUT_DIR, "lp.jpg");
  copyFileSync(lpFrom, lpTo);
  const { w, h } = dims(lpFrom);
  const home = `import type { HomeCell } from "./types";

/*
 * Home / landing page — single hero photograph (LP).
 * GENERATED by scripts/gen-2026-photos.mjs from "Updated Website Photos 7.27.26".
 */
export const homeCells: HomeCell[] = [
  {
    image: {
      src: "/images/2026/lp.jpg",
      width: ${w},
      height: ${h},
      alt: ${JSON.stringify(ALT)},
    },
  },
];
`;
  writeFileSync(path.join(CONTENT, "home.ts"), home);
  counts.landing = 1;
}

console.log("Done:", JSON.stringify(counts));
